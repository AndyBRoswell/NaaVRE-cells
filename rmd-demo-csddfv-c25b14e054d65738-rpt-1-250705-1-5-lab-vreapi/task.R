setwd('/app')
library(optparse)
library(jsonlite)



print('option_list')
option_list = list(

make_option(c("--id"), action="store", default=NA, type="character", help="my description"), 
make_option(c("--numbers"), action="store", default=NA, type="character", help="my description")
)


opt = parse_args(OptionParser(option_list=option_list))

var_serialization <- function(var){
    if (is.null(var)){
        print("Variable is null")
        exit(1)
    }
    tryCatch(
        {
            var <- fromJSON(var)
            print("Variable deserialized")
            return(var)
        },
        error=function(e) {
            print("Error while deserializing the variable")
            print(var)
            var <- gsub("'", '"', var)
            var <- fromJSON(var)
            print("Variable deserialized")
            return(var)
        },
        warning=function(w) {
            print("Warning while deserializing the variable")
            var <- gsub("'", '"', var)
            var <- fromJSON(var)
            print("Variable deserialized")
            return(var)
        }
    )
}

print("Retrieving id")
var = opt$id
print(var)
var_len = length(var)
print(paste("Variable id has length", var_len))

id <- gsub("\"", "", opt$id)
print("Retrieving numbers")
var = opt$numbers
print(var)
var_len = length(var)
print(paste("Variable numbers has length", var_len))

print("------------------------Running var_serialization for numbers-----------------------")
print(opt$numbers)
numbers = var_serialization(opt$numbers)
print("---------------------------------------------------------------------------------")



print("Running the cell")
average <- mean(numbers)
# capturing outputs
print('Serialization of average')
file <- file(paste0('/tmp/average_', id, '.json'))
writeLines(toJSON(average, auto_unbox=TRUE), file)
close(file)
